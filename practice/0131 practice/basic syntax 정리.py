'''
f - 문자열(string) 사용하기
: 중괄호 {} 안에 변수나 표현식을 넣어 해당 값을 출력할 수 있다. 

1. 변수를 사용하지 않는 예제

print(f"이름: 홍길동, 나이: 28")
# 출력: 이름: 홍길동, 나이: 28

2. 변수를 사용한 예제

name = "홍길동"
age = 28
print(f"이름: {name}, 나이: {age}")
# 출력: 이름: 홍길동, 나이: 28

위 코드에선 name과 age 변수를 중괄호 {} 안에 넣어서 해당 변수의 값을 문자열에 삽입하고 있다.

- 표현식 사용

print(f"1 + 1 = {1 + 1}")
# 출력: 1 + 1 = 2

- width와 height 값을 곱해서 면적을 계산 후, f-string을 통해 출력

width = 10
height = 5
print(f"넓이: {width * height} (가로: {width}, 세로: {height})")
# 출력: 넓이: 50 (가로: 10, 세로: 5)

- 소수점 형식지정
f-string 안에서 소수점 n번째 자리까지 작성한다.
.n 을 통해 원하는 자릿수까지 출력이 가능하다. f는 float 을 의미한다.
print(f'{  }') 안에서 { } 안에 :.nf 형식으로 작성한다 

: 소수점 이하 자릿수는 {변수:포맷} 형식을 사용하여 :.n 과 같은 구문을 통해 원하는 자릿수까지 출력할 수 있다.
n 지정시 소수점 n+1 번째 자리에서 반올림하여 소수점 n 번째 자리까지 출력한다.

pi = 3.141592653589793
# 소수점 이하 2자리까지 반올림하여 출력
print(f"파이: {pi:.2f}")
# 출력: 파이: 3.14

=> pi 를 소수점 2번째 자리까지 반올림하여 출력

# 소수점 이하 4자리까지 반올림하여 출력
print(f"파이: {pi:.4f}")
# 출력: 파이: 3.1416

=> 두 번째 예제에선 소수점 4번째 자리까지 반올림하여 출력

'''

